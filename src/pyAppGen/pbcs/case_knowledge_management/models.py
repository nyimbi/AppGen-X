def resolve_owned_table(table: str) -> str:
    return table if table.startswith(f"{PBC_KEY}_") else f"{PBC_KEY}_{table}"


def resolve_table_spec(table: str) -> dict:
    owned_table = resolve_owned_table(table)
    return TABLE_BY_NAME[owned_table]


