def _field(name: str, field_type: str, **extra: object) -> dict:
    data = {"name": name, "type": field_type}
    data.update(extra)
    return data


def _table(logical_table: str, *fields: dict, relationships: tuple[dict, ...] = ()) -> dict:
    return {
        "logical_table": logical_table,
        "owned_table": f"{PBC_KEY}_{logical_table}",
        "fields": tuple(fields),
        "relationships": relationships,
    }


