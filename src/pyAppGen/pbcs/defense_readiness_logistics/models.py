def _field(name: str, sql_type: str, *, nullable: bool = False, meaning: str = "") -> dict:
    return {
        "name": name,
        "sql_type": sql_type,
        "nullable": nullable,
        "meaning": meaning or name.replace("_", " "),
    }


def _table(
    name: str,
    *,
    category: str,
    entity: str,
    purpose: str,
    columns: tuple[dict, ...],
    primary_key: tuple[str, ...] = ("id",),
) -> dict:
    return {
        "table": name,
        "category": category,
        "entity": entity,
        "purpose": purpose,
        "primary_key": primary_key,
        "fields": tuple(column["name"] for column in columns),
        "columns": columns,
        "owned_by": PBC_KEY,
        "shared_table_access": False,
    }


